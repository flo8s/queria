"""Auto-generate catalog dbt source files from datasources.yml.

Generated files:
  - models/raw/raw_{name}.sql
  - models/raw/_schema.yml
  - models/stg/stg_catalog.sql
  - dist/metadata.json (stub, only if missing)
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

import yaml

from fdl.config_schema import load_dataset_config

CATALOG_DIR = Path(__file__).resolve().parent
DATASOURCES_YML = CATALOG_DIR / "datasources.yml"
DIST_DIR = CATALOG_DIR / "dist"
RAW_DIR = CATALOG_DIR / "models" / "main" / "raw"
STG_DIR = CATALOG_DIR / "models" / "main" / "stg"

GENERATED_HEADER = "-- Auto-generated by generate_sources.py. Do not edit manually."


def load_datasources() -> list[dict]:
    with open(DATASOURCES_YML) as f:
        data = yaml.safe_load(f)
    return data["datasources"]


def generate_raw_sql(ds: dict) -> str:
    name = ds["name"]
    if ds.get("self"):
        return f"{GENERATED_HEADER}\n{{{{ read_local_metadata_json('{name}') }}}}\n"
    return f"{GENERATED_HEADER}\n{{{{ read_metadata_json('{name}') }}}}\n"


def generate_schema_yml(datasources: list[dict]) -> str:
    models = []
    for ds in datasources:
        models.append({"name": f"raw_{ds['name']}", "description": ds["description"]})
    schema = {"version": 2, "models": models}
    header = "# Auto-generated by generate_sources.py. Do not edit manually.\n"
    return header + yaml.dump(schema, default_flow_style=False, allow_unicode=True, sort_keys=False)


def generate_stg_catalog(datasources: list[dict]) -> str:
    lines = [GENERATED_HEADER, "{{ config(materialized='table') }}", ""]
    for i, ds in enumerate(datasources):
        if i > 0:
            lines.append("UNION ALL")
        lines.append(f"SELECT * FROM {{{{ ref('raw_{ds['name']}') }}}}")
    lines.append("")
    return "\n".join(lines)


def ensure_metadata_stub() -> None:
    """Generate a stub metadata.json if dist/metadata.json doesn't exist."""
    metadata_path = DIST_DIR / "metadata.json"
    if metadata_path.exists():
        return

    config = load_dataset_config(CATALOG_DIR)

    schemas = {}
    for schema_name, schema_config in config.schemas.items():
        schemas[schema_name] = {
            "title": schema_config.title,
            "tables": [],
        }

    stub = {
        "title": config.title,
        "description": config.description,
        "tags": config.tags,
        "ducklake_url": config.ducklake_url,
        "schemas": schemas,
        "lineage": {"parent_map": {}, "nodes": {}},
    }

    DIST_DIR.mkdir(parents=True, exist_ok=True)
    with open(metadata_path, "w") as f:
        json.dump(stub, f, ensure_ascii=False, indent=2)
    print(f"  generated stub: {metadata_path.relative_to(CATALOG_DIR)}")


def warn_stale_files(datasources: list[dict]) -> None:
    expected = {f"raw_{ds['name']}.sql" for ds in datasources}
    expected.add("_schema.yml")
    for path in RAW_DIR.iterdir():
        if path.name.startswith("."):
            continue
        if path.name not in expected:
            print(f"WARNING: stale file found: {path.relative_to(CATALOG_DIR)}", file=sys.stderr)


def main() -> None:
    datasources = load_datasources()

    RAW_DIR.mkdir(parents=True, exist_ok=True)
    STG_DIR.mkdir(parents=True, exist_ok=True)

    ensure_metadata_stub()

    for ds in datasources:
        path = RAW_DIR / f"raw_{ds['name']}.sql"
        path.write_text(generate_raw_sql(ds))
        print(f"  generated: {path.relative_to(CATALOG_DIR)}")

    schema_path = RAW_DIR / "_schema.yml"
    schema_path.write_text(generate_schema_yml(datasources))
    print(f"  generated: {schema_path.relative_to(CATALOG_DIR)}")

    stg_path = STG_DIR / "stg_catalog.sql"
    stg_path.write_text(generate_stg_catalog(datasources))
    print(f"  generated: {stg_path.relative_to(CATALOG_DIR)}")

    warn_stale_files(datasources)
    print("done.")


if __name__ == "__main__":
    main()
